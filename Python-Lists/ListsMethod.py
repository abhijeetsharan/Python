numbers = [1, 2, 3]

# Append (Add at the end)
numbers.append(4)
print(numbers)

# Insert at a specific index
numbers.insert(1, 5)
print(numbers)

# remove an element
numbers.remove(5)
print(numbers)

#remove last element
numbers.pop()
print(numbers)

# Reverse the list
numbers.reverse()
print(numbers)

#Sort a list
numbers = [5, 2, 9, 1]
numbers.sort()
print(numbers)